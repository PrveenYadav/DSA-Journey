#include <iostream>
using namespace std;

//Write a program that declares variables of different data types and prints their values.
int main()
{
    int integerVal = 25;
    float floatVal = 25.26;
    double doubleVal = 14.87564993;
    char charVal = 'A';
    string stringVal = "Hello!";

    cout<<"Integer Value is: " << integerVal <<endl;
    cout<<"Float Value is: " << floatVal <<endl;
    cout<<"Double Value is: " << doubleVal <<endl;
    cout<<"Character Value is: " << charVal <<endl;
    cout<<"String Value is: " << stringVal;
}